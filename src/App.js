// ================================================
// ГЛАВНЫЙ ФАЙЛ ВИКТОРИНЫ "СТОЛИЦЫ МИРА"
// ================================================
// Этот файл содержит всю логику работы викторины:
// - переключение между вопросами
// - запись ответов пользователя
// - подсчёт правильных ответов
// - показ результата
// ================================================

// Импортируем библиотеку React (она нужна для создания компонентов)
import React, { useState } from 'react';

// Импортируем файл со стилями (CSS)
import './style.css';

// Импортируем вопросы из JSON-файла (массив с вопросами и вариантами ответов)
import voprosi from './voprosi.json';

// Это главная функция-компонент. Она возвращает HTML-код, который увидит пользователь
function App() {
  // ================================================
  // ХРАНЕНИЕ ДАННЫХ (СОСТОЯНИЯ)
  // ================================================
  // useState - это специальная функция React.
  // Она создаёт переменную, которая "запоминает" своё значение между перерисовками экрана.
  
  // now - номер текущего вопроса (0 - первый вопрос, 1 - второй и т.д.)
  // setNow - функция, которая меняет этот номер
  const [now, setNow] = useState(0);
  
  // answers - массив ответов пользователя.
  // Например, если пользователь на 1-й вопрос ответил вариантом под номером 0,
  // то answers[0] = 0. Изначально все ответы = null (не выбраны)
  const [answers, setAnswers] = useState(Array(voprosi.length).fill(null));
  
  // results - флаг, закончилась ли викторина.
  // false = викторина идёт, показываем вопросы
  // true = викторина закончена, показываем результат
  const [results, setResults] = useState(false);
  
  // message - текст, который будет показан в конце (результат)
  const [message, setMessage] = useState('');

  // ================================================
  // ФУНКЦИЯ, КОТОРАЯ ВЫЗЫВАЕТСЯ ПРИ НАЖАТИИ НА ОТВЕТ
  // ================================================
  // answerIndex - номер выбранного варианта (0, 1, 2 или 3)
  const handleAnswerClick = (answerIndex) => {
    // Создаём КОПИЮ массива answers (нельзя изменять оригинал напрямую)
    const newAnswers = [...answers];
    
    // Запоминаем, какой ответ выбрал пользователь на ТЕКУЩИЙ вопрос (now)
    newAnswers[now] = answerIndex;
    
    // Сохраняем обновлённый массив обратно в состояние
    setAnswers(newAnswers);
    
    // Вычисляем номер следующего вопроса
    const nextQuestion = now + 1;
    
    // Проверяем: есть ли ещё вопросы?
    if (nextQuestion < voprosi.length) {
      // Если да - переключаемся на следующий вопрос
      setNow(nextQuestion);
    } else {
      // Если вопросы закончились:
      // 1. Ставим флаг, что викторина закончена (покажем результат)
      setResults(true);
      // 2. Вызываем функцию подсчёта результата, передаём ей массив ответов
      calculateResult(newAnswers);
    }
  };

  // ================================================
  // ФУНКЦИЯ ПОДСЧЁТА РЕЗУЛЬТАТА
  // ================================================
  // userAnswers - массив ответов пользователя (кто на что ответил)
  const calculateResult = (userAnswers) => {
    // Счётчик правильных ответов (начинается с 0)
    let correctCount = 0;
    
    // Проходим в цикле по всем вопросам
    for (let i = 0; i < voprosi.length; i++) {
      // У каждого вопроса правильный ответ ВСЕГДА под индексом 0
      // (первый вариант в списке options)
      const correctAnswerIndex = 0;
      
      // Если пользователь выбрал первый вариант (0) - ответ правильный
      if (userAnswers[i] === correctAnswerIndex) {
        correctCount++;  // Увеличиваем счётчик на 1
      }
    }
    
    // Теперь выбираем сообщение в зависимости от количества правильных ответов
    let resultMessage = '';
    
    if (correctCount === 7) {
      resultMessage = '🎉 Идеально! Вы настоящий географ! 🌍';
    } else if (correctCount === 6) {
      resultMessage = '👍 Отлично! Всего одна ошибка!';
    } else if (correctCount === 5) {
      resultMessage = '📚 Хорошо! Но есть куда расти.';
    } else if (correctCount === 4) {
      resultMessage = '😊 Неплохо. Повторите столицы ещё раз!';
    } else if (correctCount === 3) {
      resultMessage = '🤔 Тройка. Карта мира вам в помощь! 🗺️';
    } else if (correctCount === 2) {
      resultMessage = '😬 Мало. Давайте пересдадим?';
    } else if (correctCount === 1) {
      resultMessage = '😭 Очень плохо. Нужно учить столицы!';
    } else {
      resultMessage = '💀 0 из 7. Это провал...';
    }
    
    // Добавляем в конец сообщения количество правильных ответов в скобках
    // Например: "Идеально! (7 из 7)"
    resultMessage = `${resultMessage} (${correctCount} из ${voprosi.length})`;
    
    // Сохраняем сообщение в состояние, чтобы показать пользователю
    setMessage(resultMessage);
  };

  // ================================================
  // ФУНКЦИЯ ПЕРЕЗАПУСКА ВИКТОРИНЫ
  // ================================================
  // Вызывается, когда пользователь нажимает кнопку "Начать заново"
  const restartQuiz = () => {
    setNow(0);                                      // Возвращаемся к первому вопросу (индекс 0)
    setAnswers(Array(voprosi.length).fill(null));  // Очищаем все ответы (вместо null)
    setResults(false);                              // Прячем блок с результатом
    setMessage('');                                 // Очищаем сообщение
  };

  // ================================================
  // ЧТО ПОКАЗЫВАТЬ НА ЭКРАНЕ (HTML-код)
  // ================================================
  return (
    // Главный контейнер викторины (стили берутся из style.css)
    <div className="quiz-container">
      <h1>🌍 Столицы мира 🗺️</h1>
      
      {/* Условный рендеринг: если results = true, показываем результат */}
      {results ? (
        // ============ БЛОК РЕЗУЛЬТАТА ============
        <div className="result-message">
          <h2>Ваш результат:</h2>
          <p>{message}</p>
          <button className="restart-button" onClick={restartQuiz}>
            🔄 Начать заново
          </button>
        </div>
      ) : (
        // ============ БЛОК ВОПРОСОВ ============
        <>
          {/* Контейнер для флага (картинка страны) */}
          <div className="flag-container">
            {/* 
              Картинка флага. 
              src = путь к файлу. / - означает "из папки public"
              flag - это имя файла, которое мы добавили в JSON
            */}
            <img 
              src={`/${voprosi[now].flag}`} 
              alt="флаг страны"
              className="flag-image"
            />
          </div>
          
          {/* Текст текущего вопроса */}
          <h2>{voprosi[now].question}</h2>
          
          {/* Контейнер для кнопок с вариантами ответов */}
          <div className="options-container">
            {/* 
              Перебираем массив options (варианты ответов) и для каждого создаём кнопку
              option - текст варианта (например, "Париж")
              index - номер варианта (0, 1, 2, 3)
            */}
            {voprosi[now].options.map((option, index) => (
              <button
                key={index}                     // Уникальный ключ для React
                onClick={() => handleAnswerClick(index)}  // Что делать при нажатии
                disabled={answers[now] !== null}  // Если уже ответили - кнопка неактивна
              >
                {option}  
              </button>
            ))}
          </div>
          
          {/* Счётчик: какой сейчас вопрос из скольки */}
          <p>Вопрос {now + 1} из {voprosi.length}</p>
        </>
      )}
    </div>
  );
}

// Экспортируем компонент, чтобы его можно было использовать в index.js
export default App;